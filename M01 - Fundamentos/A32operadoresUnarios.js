//Sao operadores que tem apenas um operando.
let num1 = 1
let num2 = 2
num1++ 
num2--//Acrescenta uma unidade à variavel. Esta em forma posfixada onde o operador vem depois da variavel.
console.log(num1)
console.log(num2)
--num1
++num2//Retira uma unidade à variavel. Esta em forma prefixada onde o operador vem depois da variavel.
console.log(num1)
console.log(num2)
